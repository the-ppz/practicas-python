a = 60
b = 10
c = 40

# AND
if a > b and a > c:
    print("El mayor es A")
    
if b > a and b > c:
    print("El mayor es B")
    
if c > a and c > b:
    print("El mayor es C")

# OR
if a > b or a > c:
    print("El mayor es A")
    
if b > a or b > c:
    print("El mayor es B")
    
if c > a or c > b:
    print("El mayor es C")
    
# OR CON ELIF
if a > b or a > c:
    print("El mayor es A")
    
elif b > a or b > c:
    print("El mayor es B")
    
elif c > a or c > b:
    print("El mayor es C")